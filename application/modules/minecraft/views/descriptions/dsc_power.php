<h4 class="heading">Apraksts</h4>

<p>Faction power pakalpojums nodrošina papildus power punktus tavai frakcijai.</p>
<p>Frakcijas power pakalpojuma atkārtota pirkšana nesumē tava, jau iegūtā power daudzumu. Nopērkot vispirms 10 Power un pēc tam 20 Power kopsummā tiks uzstādīts 20 Power daudzums, loģisko 30 Power vietā.</p>
